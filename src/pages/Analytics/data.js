// Helper function to get time range data
export const getTimeRangeData = (data, range) => {
  const now = new Date();
  const cutoffDate = new Date();
  
  switch (range) {
    case '6months':
      cutoffDate.setMonth(now.getMonth() - 6);
      break;
    case '1year':
      cutoffDate.setFullYear(now.getFullYear() - 1);
      break;
    case '2years':
      cutoffDate.setFullYear(now.getFullYear() - 2);
      break;
    default:
      cutoffDate.setFullYear(now.getFullYear() - 1);
  }
  
  return Object.entries(data).filter(([dateString]) => {
    const date = new Date(dateString + '-01');
    return date >= cutoffDate;
  }).reduce((acc, [key, value]) => {
    acc[key] = value;
    return acc;
  }, {});
};